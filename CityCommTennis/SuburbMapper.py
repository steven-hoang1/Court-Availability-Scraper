class SuburbMapper:
    @staticmethod
    def Map(facilityNumber):
        if facilityNumber == 1:
            return "Surry Hills"
        elif facilityNumber == 2:   
            return "Alexandria"
        elif facilityNumber == 3:
            return "Beaconsfield"
        elif facilityNumber == 4:
            return "Glebe"
        elif facilityNumber == 5:
            return "Rosebery"