class SuburbMapper:
    @staticmethod
    def Map(locationNumber):
        if locationNumber == 2:
            return "Surry Hills"
        elif locationNumber == 3:   
            return "Alexandria"
        elif locationNumber == 4:
            return "Beaconsfield"
        elif locationNumber == 5:
            return "Glebe"
        elif locationNumber == 6:
            return "Rosebery"